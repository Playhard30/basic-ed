                        Dr. Aturo O. Orosco, Jr., succeeding his mother as school president in 2012, steered the school
                        system into a brand where bayanihan spirit cultivates the deep heritage of the school. Bold
                        moves and decisions are continuously being made to meet the new leadership’s branding
                        innovations and to meet the founders’ objective of serving more communities, and societies, at
