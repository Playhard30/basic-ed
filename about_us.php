            <h1 class="headercontainer" style="color:black;"><b>School Logo</b></h1>

            <p style="color:black;">Our school logo is in the form of two superimposed circles, the larger outer one of
                a solid red field and the narrower inner circle of a white field.
                The color red dominates the logo design, red symbolizing vibrance and energy, the theme color of Saint
                Francis of Assisi College.
                The name of our school, Saint Francis of Assisi College, appears on the upper half-portion of the outer
                circle off the logo. Our school is named after our patron, Saint Francis of Assisi.
                At the lower middle of our logo is the Cross, where our Lord Jesus Christ was nailed and died to save
                the sins of humanity. On the cross hangs the “stole”, a part of the garment worn by catholic priests
                during the celebration of the Holy Mass and during blessings.
                (Saint Francis of Assisi College is a non-sectarian institution of learning, but is Catholic- oriented
                in its foundation, policies, and practices.)
                Below the Cross are the words “Fides et Humilitas”, or Faith and Humility, two virtues exemplified by
                Saint Francis of Assisi and which also serve as foundation virtues of our school.
                Above the Cross is an open and outstretched human hand, which signifies openness and reception to
                learning, as well as recognition of the dignity of human life and all things that are living –
                reflective of the virtues of Saint Francis of Assisi.
                A dove, symbolic of peace, rests on the open and outstretched human hand.
                On top of the inner circle of the logo are rays of light and energy, coming from a live and burning urn
                that illuminate the world and symbolize enlightenment as a result of learning.
                On the edges of the inner circle, starting from the base and going upward, are laurel leaves,
                symbolizing excellence as a product of hard work and dedication, as was in ancient Greece, where
                outstanding human skills and endeavors were rewarded with crowns of laurel leaves.
                The year 1981 appears at the base of the outer circle, the founding year of Saint Francis of Assisi
                College.</p>

